<template>
  <ul class="switches">
    <li
      class="switch-item"
      v-for="(item,index) in switches"
      :key="item.name"
      :class="{'active':currentIndex === index}"
      @click="switchItem(index)"
    >
      <span>{{item.name}}</span>
    </li>
  </ul>
</template>

<script>
export default {
  name: "switches",
  props: {
    switches: {
      type: Array,
      default: function() {
        return [];
      }
    },
    currentIndex: {
      type: Number,
      default: 0
    }
  },
  methods: {
    switchItem(index) {
      console.log("我点击了");
      this.$emit('switches',index)
    }
  }
};
</script>

